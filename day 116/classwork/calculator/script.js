class Calculator {
    static plus(a, b) {
      return a + b;
    }
  
    static minus(a, b) {
      return a - b;
    }
  
    static multiply(a, b) {
      return a * b;
    }
  
    static divide(a, b) {
      return a / b;
    }
  }
  
 
  console.log(Calculator.plus(5, 3));      
  console.log(Calculator.minus(10, 4)); 
  console.log(Calculator.multiply(6, 7));  
  console.log(Calculator.divide(8, 2));    
